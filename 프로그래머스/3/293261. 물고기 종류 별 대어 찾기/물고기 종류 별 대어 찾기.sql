WITH MAX_FISH_INFO AS ( -- 물고기 종류별 가장 큰 물고기의 크기
  SELECT FISH_TYPE, MAX(LENGTH)
  FROM FISH_INFO
  GROUP BY FISH_TYPE
)

SELECT ID, FISH_NAME, LENGTH
FROM FISH_INFO
JOIN FISH_NAME_INFO USING (FISH_TYPE)
WHERE (FISH_INFO.FISH_TYPE, LENGTH) IN (SELECT * FROM MAX_FISH_INFO) -- 서브 쿼리가 1개 이상의 행을 반환하므로 IN 사용
ORDER BY ID